package register;

public class Contact {
	private Integer tel;
	private Integer cel;
	private String email;
	
	public Integer getTel() {
		return tel;
	}
	public void setTel(Integer tel) {
		this.tel = tel;
	}
	
	public Integer getCel() {
		return cel;
	}
	public void setCel(Integer cel) {
		this.cel = cel;
	}
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
}
